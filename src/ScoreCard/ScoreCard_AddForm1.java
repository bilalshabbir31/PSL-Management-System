    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        String s=matchid.getText();
        boolean check=Check_ID(s);
        if(check==true){
            try{
                Connection=objConnectivity.getConnection(); 
                PreparedStatement preparedStatement=Connection.prepareStatement("select Sea.seasonId,M.MatchId, M.date,M.Time,M.MatchType,T.TeamId,Td.TeamId from MatchShedule M left join Stadium S on M.StadiumId=S.StadiumId left join TeamMatch Tm on M.MatchId=Tm.MatchId left join Team T on T.TeamId=Tm.TeamAId full join Team Td on Td.TeamId=Tm.TeamBId left join Season Sea on Sea.seasonId=Tm.seasonId where m.MatchId=?");
                preparedStatement.setString(1, s);
                ResultSet objset=preparedStatement.executeQuery();
                if(objset!=null &&objset.next()){
                    SheduleMatch sm=new SheduleMatch(objset.getString(1),objset.getString(2),objset.getDate(3),objset.getString(4),objset.getString(5),objset.getString(6),objset.getString(7));
                    RunNextFrame(sm);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,e);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Sorry this Match isn't Found in Database! ");
        }
        
    }//GEN-LAST:event_searchActionPerformed

