    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
        int seatno=Integer.parseInt(seat.getText());
        String pname=playername.getText();
        long number=Long.parseLong(phonenumber.getText());
        String Aname=airportname.getText();
        String Aline=airlinename.getText();
        String Afrom=from.getText();
        String Ato=to.getText();
        java.util.Date date= flightdate.getDate();
        java.sql.Date d=new java.sql.Date(date.getTime());
        String dtime=departuretime.getText();
        String darrive=arrivaltime.getText();
        if(!(pname.isEmpty()||Aname.isEmpty()||Aline.isEmpty()||Afrom.isEmpty()||Ato.isEmpty()||dtime.isEmpty()||darrive.isEmpty())){
        
        
            connection=object.getConnection();
         AirLinedal dal=new AirLinedal(connection);  
         
        AirLine airl=new AirLine(seatno,pname,number,Aname,Aline,Afrom,Ato,d,dtime,darrive);
        dal.AddRecord(airl);
        
        
        }
        else if(pname.isEmpty()||Aname.isEmpty()||Aline.isEmpty()||Afrom.isEmpty()||Ato.isEmpty()||dtime.isEmpty()||darrive.isEmpty())
        {
          JOptionPane.showMessageDialog(this, "Fill All the Requirements");
        
        }
        
        
        }catch(Exception e)
                {
                  e.printStackTrace();
                
                
                }
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

