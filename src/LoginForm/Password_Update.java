    private void updateShedulerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateShedulerActionPerformed

        // get data in local variables
        String password=employeepassword.getText();
        String cpassword=employeecpassword.getText();
        if(password.equals(cpassword)){
                            
            try{
                Connection=objConnectivity.getConnection();
                String query="Update CreateAccount set password=? Where username=?";
                PreparedStatement preparedStatement = Connection.prepareStatement(query);
                preparedStatement.setString(1,password);
                preparedStatement.setString(2,Employeeusername.getText());
                preparedStatement.executeUpdate();
                JOptionPane.showMessageDialog(this,"Update Succesfully!");
                setClear();
                runPreviousFrame();
            }catch(Exception e){
                e.printStackTrace();
            }
        }else{
            JOptionPane.showMessageDialog(this,"Your Password is n't Match! ");
        }



    }//GEN-LAST:event_updateShedulerActionPerformed

