    private void ownerdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ownerdeleteActionPerformed
        if(option.getSelectedItem()=="Option"){
            JOptionPane.showMessageDialog(this, "Select Only One Menu Please!");
        }
        else if(option.getSelectedItem().equals("ID")){
            if(!(delete.getText().isEmpty())){
                int id=Integer.parseInt(delete.getText());
                Connection=objConnectivity.getConnection();
                OwnerDal dal=new OwnerDal(Connection);
                boolean check=dal.checkOwner_id(id);
                if(check==true){
                    dal.DeletebyID(id);
                    setClear();
                }else{
                    JOptionPane.showMessageDialog(this,"Sorry This Owner is'nt Found Database! ");
                }
            }else{
                 JOptionPane.showMessageDialog(this,"Please Fill Field! ");
            }
        }
        else if(option.getSelectedItem().equals("Name")){
            if(!(delete.getText()).isEmpty()){
                String Name=delete.getText();
                Connection=objConnectivity.getConnection();
                OwnerDal dal=new OwnerDal(Connection);
                boolean check=dal.check_Owner_Name(Name);
                if(check==true){
                    dal.DeletebyName(Name);
                    setClear();
                }else{
                    JOptionPane.showMessageDialog(this,"Sorry This Owner is'nt Found Database! ");
                }
            }else{
                JOptionPane.showMessageDialog(this,"Please Fill Field! ");
            }
        }
    }//GEN-LAST:event_ownerdeleteActionPerformed

