    public void Search(){
        try{
        connection=objConnectivity.getConnection();
        String categoryName=txt_Search.getText();
        String query="select  *from Category where categoryName= ? ";
        PreparedStatement objPrepareStatement=connection.prepareStatement(query);
        Category objCategory=new Category(categoryName);
        objPrepareStatement.setString(1,objCategory.getCategoryName());
        ResultSet objResult=objPrepareStatement.executeQuery();
    
        if(objResult.next()){
        objCategory.setCategoryId(objResult.getString("categoryId"));
        objCategory.setCategoryName(objResult.getString("categoryName"));
        objCategory.setCategoryPrice(Integer.parseInt(objResult.getString("price")));
          JOptionPane.showMessageDialog(this,"Name  "+objCategory.getCategoryName()+"Id   "+objCategory.getCategoryId()+" "+" Worth   "+objCategory.getCategoryPrice());
                   }else{
            JOptionPane.showMessageDialog(this,"Not Found");
        }
             }catch(Exception e){
                    e.printStackTrace();
                }
    }
