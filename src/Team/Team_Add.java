    private void addteamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addteamActionPerformed
        // get Data into Local Variables
        if(!(Teamid.getText().isEmpty()&&Teamname.getText().isEmpty()&&Teamofficecontact.getText().isEmpty()&&Teammail.getText().isEmpty())){
            String id=Teamid.getText();
            String Name=Teamname.getText();
            String Mail=Teammail.getText();
            long contact=Long.parseLong(Teamofficecontact.getText());
            java.util.Date utilStartDate = teamfoundeddate.getDate();
            java.sql.Date d = new java.sql.Date(utilStartDate.getTime());
            //int owner_id =Integer.parseInt(ownerid.getText());
            Connection=objConnectivity.getConnection();
            if(owner.getSelectedItem()!="-Select-"){
                OwnerDal dal=new OwnerDal(Connection);
                Owner o=dal.SearchbyName((String) owner.getSelectedItem());
                TeamDal Teamdal=new TeamDal(Connection);
                boolean checkTeamID=Teamdal.check_Team_id(id);
                if(checkTeamID!=true){
                        Team T=new Team(id, Name, Mail, contact, d, o.getId());
                        Teamdal.Add(T);
                        setClear();
                }        
                else{
                    JOptionPane.showMessageDialog(this,"Sorry Team,ID is Already Found in Database! ");
                }
            }else{
                JOptionPane.showMessageDialog(this,"Sorry Please Select One Owner of Team ");

            }

            
        }
        
        
        

    }//GEN-LAST:event_addteamActionPerformed

